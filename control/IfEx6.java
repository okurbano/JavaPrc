class IfEx6
{
	public static void main(String ar[])
	{
		int    num;
		num= Integer.parseInt(ar[0]);

		if(num==2)
		{
			System.out.println(" Green  - Condition1");
		}
		else if(num==4)
		{
			System.out.println(" red  - Condition2");
		}
		else if(num==6)
		{
			System.out.println(" yellow  - Condition3");
		}
		 else if(num==9)
		{
			System.out.println(" Blue  - Condition4");
		}
	}

}
		






